#Student Marks Manager
marks=[85,92,88,79,95]
print("Student Marks:")
print(marks)

#Count The students

print("Total Students:",len(marks))

#Find Highest Mark
print("The Highest Mark:",max(marks))

#print Lowest Mark
print("The Lowest mark:",min(marks))

#Find Average
average=sum(marks)/len(marks)
print(average)

print("All Students marks:")
for mark in marks:
    print(mark)

